public class Condicionais {
    public static void main(String[] args) {
        if(3<2) {
            System.out.println("3 é maior que 2");
        }else if(2 == 2) {
            System.out.println("2 é igual a 2");
        }else {
            System.out.println("3 não é menor que 2");
        }
    }
}
